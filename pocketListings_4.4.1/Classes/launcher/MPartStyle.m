#import "MPartStyle.h"
#import "MStyleContext.h"
#import "MCorePreprocessorMacros.h"

@implementation MPartStyle
@synthesize name  = _name;
@synthesize style = _style;


///////////////////////////////////////////////////////////////////////////////////////////////////
- (void)dealloc {
    TT_RELEASE_SAFELY(_name);
    TT_RELEASE_SAFELY(_style);
    
    [super dealloc];
}


///////////////////////////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////////////////////////
#pragma mark -
#pragma mark Class public


///////////////////////////////////////////////////////////////////////////////////////////////////
+ (MPartStyle*)styleWithName:(NSString*)name style:(MStyle*)stylez next:(MStyle*)next {
    MPartStyle* style = [[[self alloc] initWithNext:next] autorelease];
    style.name = name;
    style.style = stylez;
    return style;
}


///////////////////////////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////////////////////////
#pragma mark -
#pragma mark MStyle


///////////////////////////////////////////////////////////////////////////////////////////////////
- (void)draw:(MStyleContext*)context {
    [self.next draw:context];
}


///////////////////////////////////////////////////////////////////////////////////////////////////
- (void)drawPart:(MStyleContext*)context {
    [_style draw:context];
}


@end
